package cn.cuit.lsn.pojo;

public class MovieInfo {

    public MovieInfo(){}

    public String title;
    public String genres;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getGenres() {
        return genres;
    }

    public void setGenres(String genres) {
        this.genres = genres;
    }
}
